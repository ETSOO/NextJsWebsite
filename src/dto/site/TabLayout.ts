/**
 * Tab layout
 * 栏目布局
 */
export enum TabLayout {
    /**
     * Display one article
     * 显示一篇文字
     */
    Article,

    /**
     * No display
     * 无任何显示
     */
    None,

    /**
     * Title list
     * 标题列表
     */
    TitleList,

    /**
     * Title & description list
     * 标题和描述列表
     */
    TitleDescriptionList,

    /**
     * Logo & title list
     * 图片和标题列表
     */
    LogoList,

    /**
     * Title, logo & description list
     * 标题、图片和描述列表
     */
    FullList,

    /**
     * Title, logo & content list
     * 标题、图片和内容列表
     */
    ContentList
}
